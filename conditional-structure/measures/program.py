a = float(input("Enter measure A: "))
b = float(input("Enter measure B: "))
c = float(input("Enter measure C: "))

squareArea = a * a
triangleArea = (a * b) / 2
trapezioArea = ((a + b) * c) / 2

print(f"SQUARE AREA = {squareArea:.4f}")
print(f"TRIANGLE AREA = {triangleArea:.4f}")
print(f"TRAPEZIO AREA = {trapezioArea:.4f}")

